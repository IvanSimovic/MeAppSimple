package com.simovic.meappsimple.base

import android.app.Application
import com.simovic.meappsimple.BuildConfig
import com.simovic.meappsimple.feature.album.featureAlbumModules
import com.simovic.meappsimple.feature.birthday.featureBirthDayModules
import com.simovic.meappsimple.feature.livefeed.featureLiveFeedModules
import org.koin.android.ext.koin.androidContext
import org.koin.android.ext.koin.androidLogger
import org.koin.core.context.GlobalContext
import timber.log.Timber

class Application : Application() {
    override fun onCreate() {
        super.onCreate()

        initKoin()
        initTimber()
    }

    private fun initKoin() {
        GlobalContext.startKoin {
            androidLogger()
            androidContext(this@Application)

            modules(appModule)
            modules(featureAlbumModules)
            modules(featureLiveFeedModules)
            modules(featureBirthDayModules)
        }
    }

    private fun initTimber() {
        if (BuildConfig.DEBUG) {
            Timber.plant(Timber.DebugTree())
        }
    }
}
